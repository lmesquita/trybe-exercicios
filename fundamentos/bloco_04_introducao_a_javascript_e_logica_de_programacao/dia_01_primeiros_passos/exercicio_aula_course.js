const a = 20;
const b = 10;

let qualOperacao = "Módulo";

switch (qualOperacao) {

    case "Adição":
        console.log(a + b);
        break;

    case "Subtração":
        console.log(a - b);
        break;

    case "Multiplicação":
        console.log(a * b);
        break;

    case "Divisão":
        console.log(a / b);
        break;
        
    case "Módulo":
        console.log(a % b);
        break;

}